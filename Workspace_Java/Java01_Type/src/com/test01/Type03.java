package com.test01;

public class Type03 {
	
	// 실수형 리터럴 : 소수점을 포함한 수
	// float(4byte), 기본타입 double(8byte)
	public static void main(String[] args) {
		float f01 = 0.1f;
		float f02 = 9.8F;
		float fSum = f01 + f02;
		System.out.println(fSum);
		
		double d01 = 100.04;
		double d02 = 9.9D;
		double dSum = d01 + d02;
		System.out.println(dSum);
	
	}
}
