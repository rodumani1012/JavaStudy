package com.test01;

public class Type04 {

	// 문자형, 문자열, 논리형
    // char(2), String(), boolean(1)
	public static void main(String[] args) {
		char c01 = 'a';
		System.out.println(c01);
		
		String s01 = "a";
		System.out.println(s01);
		String s02 = s01 + "b";
		System.out.println(s02);
		
		System.out.println("\ucd5c\uc900\uc5f0");
		
		boolean bl01 = true;
		boolean bl02 = false;
		
		System.out.println(bl01);
		System.out.println(bl02);
		
		
	}

}
